package com.genesis.genesisapi.model;

public class OutgoingReport {
	
	private String dlNo;
	private String dateOfDelivery;
	private String wsoNo;
	private String lotNo;
	private String ttlQty;
	private String ttlWt;
	public String getDlNo() {
		return dlNo;
	}
	public void setDlNo(String dlNo) {
		this.dlNo = dlNo;
	}
	public String getDateOfDelivery() {
		return dateOfDelivery;
	}
	public void setDateOfDelivery(String dateOfDelivery) {
		this.dateOfDelivery = dateOfDelivery;
	}
	public String getWsoNo() {
		return wsoNo;
	}
	public void setWsoNo(String wsoNo) {
		this.wsoNo = wsoNo;
	}
	public String getLotNo() {
		return lotNo;
	}
	public void setLotNo(String lotNo) {
		this.lotNo = lotNo;
	}
	public String getTtlQty() {
		return ttlQty;
	}
	public void setTtlQty(String ttlQty) {
		this.ttlQty = ttlQty;
	}
	public String getTtlWt() {
		return ttlWt;
	}
	public void setTtlWt(String ttlWt) {
		this.ttlWt = ttlWt;
	}
	
	
	
	
}
